using Content.Client.UserInterface.Controls;
using Content.Shared._CorvaxNext.ModularComputers;
using Content.Shared._CorvaxNext.ModularComputers.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._CorvaxNext.ModularComputers.UI;

[GenerateTypedNameReferences]
public sealed partial class ModularComputerUI : FancyWindow
{
    [Dependency] private IEntityManager _entManager = null!;
    [Dependency] private ModularComputersSystem _modComps = null!;

    public ModularComputerUI()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public void Initialize(EntityUid modcomp)
    {
        var mod = _entManager.GetComponent<ModularComputerComponent>(modcomp);
        if (_modComps.TryGetPciComponent(mod, out PciGpuComponent? gpu, out _))
            GpuRender.GpuComponent = gpu;
    }
}

